const searchForm = document.getElementById("searchInput");
searchForm.addEventListener("submit", (event) => {
  event.preventDefault();
  const searchValue = document.getElementById("searchInputValue").value;
  const userId = parseInt(searchValue);
  if (userId > 1 && userId <= 10) {
    window.location.href = `posts.html?id=${userId}`;
  } else {
    alert("Hoopp Emmioglu Dikkat et");
  }
});

